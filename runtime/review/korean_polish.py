# 법률 문어체 접속사/어미 정규화
_REGEX_FORMAL: list[tuple[str, str]] = [
    (r"하고\s*있다\b", "하고 있다"),
    (r"되어\s*있다\b", "되어 있다"),
    (r"([가-힣])\s*이고\b", r"\1이며"),
    (r"([가-힣])\s*이지만\b", r"\1이나"),
    (r"([가-힣])\s*하지만\b", r"\1하나"),
    (r"([가-힣])\s*그리고\b", r"\1하고"),
    (r"\b그러나\b", "다만"),
    (r"\b하지만\b", "다만"),
    (r"\b또한\b", "또한"),   # 유지
    (r"([가-힣])\s*때문에\b", r"\1으로 인하여"),
    (r"([가-힣])\s*때문\b", r"\1으로 인한"),
]


def polish_korean_legal_style(text: str) -> str:
    """
    구어체·모호 표현을 고도의 법률 문어체로 변환한다.
    시디즈 사내 변호사 기준: 대리점법 제18조 경영간섭 방어 표현 포함.
    """
    s = (text or "").strip()
    if not s:
        return ""

    # 1. 공백 정규화
    s = re.sub(r"[ \t]+", " ", s)
    s = re.sub(r"\n{3,}", "\n\n", s)

    # 2. 조사 오류 교정
    s = s.replace("상대방는", "상대방은").replace("상대방를", "상대방을").replace("상대방가", "상대방이")
    s = s.replace("당사은", "당사는").replace("당사가", "당사가").replace("당사를", "당사를")
    s = s.replace("상대방 및 그", "상대방과 그")

    # 3. 내부 메타 레이블 제거
    s = re.sub(r"(구매자 보호 방향\([^)]*\)\s*기준으로\s*보완:\s*)", "", s)
    s = re.sub(r"(buyer_favorable\s*기준으로\s*보완[:：]?\s*)", "", s, flags=re.IGNORECASE)
    s = re.sub(r"(구매자 보호\s*중심으로\s*보완[:：]?\s*)", "", s)

    # 4. 구어체 → 법률 문어체 치환 (테이블 순서 보장)
    for colloquial, formal in _COLLOQUIAL_TO_FORMAL:
        s = s.replace(colloquial, formal)

    # 5. 정규식 기반 문어체 변환
    for pattern, replacement in _REGEX_FORMAL:
        s = re.sub(pattern, replacement, s)

    # 6. 괄호·구두점 공백 정리
    s = re.sub(r"\s+([,.)])", r"\1", s)
    s = re.sub(r"([(\[])\s+", r"\1", s)

    # 7. 문장 끝 마침표 보장 (단, 괄호로 끝나는 경우 제외)
    stripped = s.rstrip()
    if stripped and not stripped.endswith((".", "다", "함", ")", "]", ".")):
        s = stripped + "."
    else:
        s = stripped

    return s.strip()

